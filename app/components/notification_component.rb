# frozen_string_literal: true

class NotificationComponent < ViewComponent::Base
  def initialize(notification:)
    @notification = notification
  end

  def read_status_link_color_class
    if @notification.read?
      'text-nav-link-read'
    else
      'text-nav-link-unread'
    end
  end

